#include "ft_printf.h"

void	ft_rightpad(t_b *b, t_d *d, char c)
{
	while (d->w-- > 0)
		b->head += ft_fwritebuff(b, c);
	while (d->p-- > 0)
		b->head += ft_fwritebuff(b, '0');
}
